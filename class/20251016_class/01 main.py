# –ü–µ—Ä–µ–¥–∞—á–∞ –¥–∞–Ω–Ω—ã—Ö –º–µ–∂–¥—É —Ä–∞–∑–Ω—ã–º–∏ –∫–ª–∞—Å—Å–∞–º–∏ (–∫–æ–º–ø–æ–∑–∏—Ü–∏—è, –ø–µ—Ä–µ–¥–∞—á–∞ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞/—Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞). üü†  
# –ü—Ä–∞–∫—Ç–∏—á–µ—Å–∫–∏–π –ø—Ä–∏–º–µ—Ä: –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –≤ –æ–¥–Ω–æ–º –∫–ª–∞—Å—Å–µ ‚Üí –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –≤ –¥—Ä—É–≥–æ–º. üü£

# –°–æ–±—Å–æ–± - –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ –∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –≤ –∞—Ç—Ä–∏–±—É—Ç–µ –∏ –¥–∞–ª—å–Ω–µ–π—à–∏–π –≤–æ–∑–≤—Ä–∞—Ç
class Calculator:
    def __init__(self, base):
        self.base = base # –∞—Ç—Ä–∏–±—É—Ç —ç–∫–∑–µ–º–ª—è—Ä–∞
        self.last = None # —Å—é–¥–∞ –º–æ–∂–Ω–æ —Å–æ—Ö—Ä–∞–Ω—è—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç

    def add(self, x):
        result = self.base + x
        self.last = result # —Å–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –∞—Ç—Ä–∏–±—É—Ç
        return result # –∏ –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º
    
    def multiply_using_last(self, factor):
        # —á–∏—Ç–∞–µ–º —Ä–∞–Ω–µ–µ —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏–∑ self.last
        if self.last is None:
            raise ValueError('–ù–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–≥–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞')
        return self.last * factor
    
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ
c = Calculator(10)
r = c.add(5)
m = c.multiply_using_last(3)
print(r, m)

